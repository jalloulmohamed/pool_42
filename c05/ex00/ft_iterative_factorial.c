int	ft_iterative_factorial(int nb)
{
	int	c;

	c = 1;
	if (nb < 0)
		return (0);
	if (nb == 1 || nb == 0)
		return (1);
	else
	{
		while (nb >= 1)
		{
			 c *= nb;
			 nb--;
		}
		return (c);
	}
}
