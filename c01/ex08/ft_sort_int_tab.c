void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	*p;
	int	*f;
	int	y;

	i = 0;
	f = tab;
	while (i <= size - 2)
	{
		p = tab + 1;
		if (*tab >= *p)
		{
			y = *tab;
			*tab = *p;
			*p = y;
			tab = f;
			i = -1;
		}
		else
		{
			tab++;
		}
		i++;
	}
}
