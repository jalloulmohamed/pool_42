char	*ft_strstr(char	*str, char	*to_find)
{
	int	i;

	i = 0;
	while (to_find[i] != '\0')
	{
		if (str[i] == to_find[i])
			i++;
		else
		{
			i = 0;
			str++;
			if (str[i] == '\0')
				return (0);
		}
	}
	return (str);
}
